    public boolean ViewExists (String s){
        //
        Iterator<CView> i = views.iterator ();
        while(i.hasNext ()){
            CView n =  i.next ();
            if(n.getTitle().equalsIgnoreCase (s)){
                return true;
            }
        }
        return false;
    }
    
