    public void RemoveView (String s){
        //synchronized(views){
        for(int i = 0; i< views.size (); i++){
            final CView v = views.get (i);
            if(v.getTitle().equals (s)){
                RemoveView(v);
                i--; //decrement so that we then get incremented and handle the same index again
            }
        }

    }
    
