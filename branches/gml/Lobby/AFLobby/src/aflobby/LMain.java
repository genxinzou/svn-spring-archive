    public void SetFocus (String s){
        //v.setVisible(true);
        Iterator<CView> i = views.iterator ();
        while(i.hasNext ()){
            CView n =  i.next ();
            if(n == null){
                System.out.println (java.util.ResourceBundle.getBundle("aflobby/languages").getString("LMain.n_==_null_in_LMain"));
                continue;
            }
            if(n.getTitle().equalsIgnoreCase (s)){
                SetFocus (n);
                return;
            }
        }
    }
    
