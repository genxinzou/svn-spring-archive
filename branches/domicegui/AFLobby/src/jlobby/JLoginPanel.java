    public void NewEvent(JEvent e){
        if(e.data[0].equalsIgnoreCase("ACCEPTED")==true){
            //jTextPane1.setText("");
            // switch to the next view
            e.connection.traffic.add("Time to add Channel View");
            JChannelView v = new JChannelView(LM);
            //v.setVisible(true)
            e.connection.traffic.add("Channel View Spawned");
            LM.AddView(v,true);
            e.connection.traffic.add("Channel View added to LM");
        }else if(e.data[0].equalsIgnoreCase("UNKNOWN_HOST")==true){
            //jTextPane1.setText("error : \n\n Unknown Host");
            //validate();
            //LM.DoValidate();
        }else if(e.data[0].equals("TASSERVER")==true){
            String p = Passbox.getPassword().toString();
            if(p == null){
                jc.traffic.add("p == null!");
                return;
            }
            String u = UserBox.getText();
            if(u == null){
                jc.traffic.add("u == null!");
                return;
            }
            e.connection.Login(u,p);
        }else if(e.data[0].equalsIgnoreCase("JLOGOUT")==true){
            LM.connection.Disconnect();
        }
        
    }
    
